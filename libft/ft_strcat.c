#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int iter;

	iter = ft_strlen(s1);
	while (*s2)
	{
		s1[iter] = *s2;
		iter++;
		s2++;
	}
	s1[iter] = '\0';
	return (s1);
}
