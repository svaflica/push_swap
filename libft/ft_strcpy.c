char	*ft_strcpy(char *dst, const char *src)
{
	int iter;

	iter = 0;
	while (src[iter] != '\0')
	{
		dst[iter] = src[iter];
		iter++;
	}
	dst[iter] = '\0';
	return (dst);
}
